#include "3-calc.h"
/**
 * op_add - returns the sum of 2 int
 * @a: first operand
 * @b: the second
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns the diff
 * @a: first operand
 * @b: second operand
 * Return: the diffrence
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns the product
 * @a: first operand
 * @b: second operand
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns division
 * @a: first op
 * @b: second op
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns the mod
 * @a: first op
 * @b: second op
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
